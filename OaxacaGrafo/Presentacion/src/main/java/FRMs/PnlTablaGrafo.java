    //a√±adir info a la tabla
    private void llenarTabla() {
        // Obtener el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);

        // Recorrer vertices
        for (Map.Entry<String, Vertice> entry : grafo.getVertices().entrySet()) {
            String nombreVertice = entry.getKey();
            Vertice vertice = entry.getValue();

            // Recorrer 
            for (Arista arista : vertice.getAristas()) {
                // Crear una fila de objetos con los datos
                Object[] fila = {
                    //origen
                    nombreVertice,
                    //destino
                    arista.getDestino().getNombre(),
                    //peso
                    arista.getPeso()
                };
                modelo.addRow(fila);
            }
        }

    }

