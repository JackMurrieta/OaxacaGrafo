    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String ciudadOrigen = cbBoxOrigen.getSelectedItem().toString();
        String ciudadDestino = cbBoxDestino.getSelectedItem().toString();

        if (ciudadOrigen.equals(ciudadDestino)) {
            Controlador.Controlador.mostrarError("El origen y destino deben ser diferentes");
            return;
        }

        Controlador.Controlador.mostrarFrmDijkstra(ciudadOrigen, ciudadDestino);
    }//GEN-LAST:event_btnAceptarActionPerformed

