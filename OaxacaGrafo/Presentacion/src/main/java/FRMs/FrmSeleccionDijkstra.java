    private void cargarVertices() {
        cbBoxOrigen.removeAllItems();
        cbBoxDestino.removeAllItems();
        for (String nombreVertice : grafo.getVertices().keySet()) {
            cbBoxOrigen.addItem(nombreVertice);
            cbBoxDestino.addItem(nombreVertice);
        }
    }

